// variable.slice(x,y) where x is Start position & y is end position

var Name = "Angela"; // Indexing: 0, 1, 2, 3, 4, 5, 6
Name.slice(0,3);        // Output = Ang     Range is till index (n-1)
Name.slice(1,3);        // Output = ng      Range is till index (n-1)

