#include<stdio.h>

int main(void) {
    printf("%d \n", -16 * 10); //This is integer, rest float
    printf("%f \n", -16.0 * 10);
    printf("%f \n", -16.0 * 10);
    printf("%f \n", -16.0 * 10.0);

    /*
    
    Topic: Type Coversion

    int OP int -> int        (Output)
    int OP float -> float    (Output)
    float OP float -> float  (Output)

    Here, OP = operation ( +, -, *, / and % for only first printf command) 

    By this time, you must have noticed & understood that when float is small it is storying values till the 6th decimal place
    
    */
   
}

